function OnLoad (executionContext) {
    debugger;
    const formContext = executionContext.getFormContext();

    OnChange_HajjCategory(formContext);
    formContext.getAttribute(hajj.ldv_hajjcategorycode).addOnChange(function () {
        OnChange_HajjCategory(formContext);
    });
};

function OnChange_HajjCategory  (formContext) {
    var hajjCategory = CommonGeneric.GetFieldValue(formContext, hajj.ldv_hajjcategorycode);
    var internalFieldsToBeShown = [hajj.ldv_nationalidnumberresidentidnumber, hajj.ldv_companions, hajj.ldv_additionalservices, hajj.ldv_hajjpermit, hajj.ldv_sacrifices, hajj.ldv_internal, hajj.ldv_printedpermit];
    var externalFieldsToBeShown = [hajj.ldv_passportnumber, hajj.ldv_visanumber, hajj.ldv_bordernumber];

    // Show or hide internal fields based on permit category
    CommonGeneric.ShowFields(formContext, internalFieldsToBeShown, hajjCategory === hajj.Enums.ldv_hajjcategorycodeEnum.Internal);

    // Show or hide external fields based on permit category
    CommonGeneric.ShowFields(formContext, externalFieldsToBeShown, hajjCategory === hajj.Enums.ldv_hajjcategorycodeEnum.External);

    // Show or hide Package Details Tab based on hajj category
    CommonGeneric.TabVisability(formContext, hajj.Tabs.packagedetails, hajjCategory === hajj.Enums.ldv_hajjcategorycodeEnum.External);

    // Show or hide Flight Details Section based on hajj category
    CommonGeneric.SectionVisibility(formContext, hajj.Tabs.hajjinformation, hajj.Sections.flightdetails, hajjCategory === hajj.Enums.ldv_hajjcategorycodeEnum.External);

};
